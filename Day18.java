public class day18 {
  public static void main(String[] args) {
    byte umur = 25;                      //nilai byte
    int umurInt = umur;                  //otomatis byte ke int
    long umurLong = umurInt;             //otomatis int ke long
    double umurDouble = umurLong;        //otomatis long ke double

    System.out.println("Umur (Byte)  : "+ umur );
    System.out.println("Umur (Int)   : "+ umurInt );
    System.out.println("Umur (Long)  : "+ umurLong);
    System.out.println("Umur (Double): "+ umurDouble);
    
  }
}
