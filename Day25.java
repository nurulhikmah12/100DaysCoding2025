public class day25 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println("Nilai x sebelum increment: " + (x)); 
        System.out.println("x++ = " + x++); // Output: 5
        System.out.println("Nilai x setelah increment: " + x); // Output: 6

        int y = 5;
        System.out.println("\nNilai y sebelum increment: " + (y)); 
        System.out.println("++y = " + ++y); // Output: 6
        System.out.println("Nilai y setelah increment: " + y); // Output: 6
    }
}
