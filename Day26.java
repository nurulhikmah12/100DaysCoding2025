public class day26 {
    public static void main(String[] args) {
        int x = 5; // penugasan nilai
        x += 3; // x = x + 3, Operator Penugasan Penjumlahan
        System.out.println("Operator penugasan penjumlahan: "+ x); 

        x -= 2; // x = x - 2, Operator Penugasan Pengurangan
        System.out.println("Operator penugasan pengurangan: "+ x); 

        x *= 2; // x = x * 2, Operator Penugasan Perkalian
        System.out.println("Operator penugasan perkalian: "+ x);

        x /= 2; // x = x / 2, Operator Penugasan Pembagian
        System.out.println("Operator penugasan pembagian: "+ x); 
    }
}
