public class day5 {
	public static void main(String[] args) { 
        // int biasanya untuk bilangan bulat umum
        int umur = 20; // umur seseorang
        System.out.println("Umur saya: " + umur + " tahun");

        // long biasanya untuk bilangan bulat yang sangat besar
        long jumlahPenduduk = 278000000L; // jumlah penduduk Indonesia
        System.out.println("Jumlah penduduk Indonesia: " + jumlahPenduduk);

        // short untuk bilangan bulat kecil, misalnya jumlah barang
        short jumlahBuku = 120; // jumlah buku di perpustakaan kecil
        System.out.println("Jumlah buku di perpustakaan: " + jumlahBuku);

        // byte untuk bilangan yang sangat kecil, misalnya nilai ujian
        byte nilaiUjian = 95; // nilai ujian siswa
        System.out.println("Nilai ujian: " + nilaiUjian);
    }
}	
	
