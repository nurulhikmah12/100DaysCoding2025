public class day7 {
	public static void main(String[] args) {
		//Tipe data karakter (Char)
		char jenisKelamin = 'P'; //P untuk perempuan dan L untuk laki-laki 
		System.out.println("Jenis Kelamin: "+ jenisKelamin); 
		
		//Tipe data Boolean 
		boolean sayaPerempuan = true; 
		//True jika saya adalah perempuan, false jika tidak 
		System.out.println("Apakah saya perempuan? "+ sayaPerempuan);
	}
}
