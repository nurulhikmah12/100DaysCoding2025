public class ContohPembulatan {

    public static void main(String[] args) {
        
        double angka1 = 5.3;
      
        System.out.println("Angka asli: " + angka1);
        System.out.println("Ceil : " + Math.ceil(angka1));   // Pembulatan ke atas
        System.out.println("Floor: " + Math.floor(angka1));  // Pembulatan ke bawah
        System.out.println("Round: " + Math.round(angka1));  // Pembulatan terdekat

    }
}
